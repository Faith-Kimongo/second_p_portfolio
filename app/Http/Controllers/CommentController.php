     * @param  \App\Http\Requests\StoreCommentRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request,Myhustle $myhustle)
    {
        $comment = new Comment;
        $comment->comment = $request->comment;
        $comment->myhustle_id = $myhustle->id; // Associate the comment with the Myhustle model
        $comment->user_id = auth()->id(); // Associate the comment with the authenticated user
        $comment->save();
    
        return back()->with('success','Successfully commented');
    }

